                    <h3 class=" Mitexto">Descripción del problema de investigación</h3><br>
                    <p class="text-muted align-text-justify">De acuerdo con datos estadísticos del informe DF-14A.xlsx que se descarga de la plataforma administrativa del SENA, SofiaPlus, durante los años 2019 y 2020, el registro de aprendices matriculados en el Programa Técnico en Programación de Software fue de 1426 y 1316 aprendices respectivamente, mientras que el total de aprendices retirados fue de 258 y 296, lo cual significa que  en 2019 un 18,1% de los aprendices desertaron o se retiraron del proceso de formación, y en el 2020 este porcentaje aumento a un  22,5%. </p>

                    <h3 class=" Mitexto mt-5">Pregunta Problematizadora</h3><br>
                    <p class="text-muted align-text-justify">¿Cómo utilizar la gamificación transversalizando el componente afectivo de las emociones, en el programa técnico en programación de software, para disminuir la deserción escolar en la educación media colombiana? </p>

                    <h3 class=" Mitexto mt-5">Justificación</h3><br>
                    <p class="text-muted align-text-justify">Interpretar el contexto de la gamificación en el marco de la educación significativa e integral teniendo en cuenta políticas institucionales.
                    La utilización de la lúdica, los juegos y herramientas tecnológicas que ya existen en nuestro entorno, llevadas a las prácticas educativas permiten desarrollar habilidades, incrementar la concentración, desarrollar la motricidad, y destacan otros aspectos como el esfuerzo, la motivación, el logro y la colaboración.
                    El cuerpo docente debe reinventarse a sí mismo, porque el mundo está cambiando, por tanto, las instituciones educativas y la educación deben adaptarse a las tendencias de la ciencia, la tecnología y el trabajo.
                    Reinventarse llevará a aprender y comprender nuevas metodologías, operaciones, tecnologías e innovaciones. Esto significa mostrar que se ha entrado en el entorno educativo con nuevos conocimientos.</p>
              
                    <h3 class=" Mitexto mt-5">Objetivo General
                    </h3><br>
                    <p class="text-muted align-text-justify">Implementar una estrategia de gamificación transversalizando el componente afectivo de la personalidad, en el programa Técnico en Programación de Software para disminuir la deserción escolar en la educación media colombiana. </p>
                    <h3 class=" Mitexto mt-5">Objetivos Específicos
                    </h3><br>
                    <p class="text-muted align-text-justify">Sistematizar los fundamentos teóricos sobre la gamificación para transversalizar el componente afectivo de la personalidad en el proceso de enseñanza y aprendizaje para disminuir la deserción escolar.<br>
                    Describir el estado actual del desarrollo emocional de alumnos de dos instituciones adscritas al programa de técnico en programación de software perteneciente al Centro de Servicios y Gestión Empresarial.<br>
                    Diseñar la estructura y los componentes de la metodología educativa gamificada a fin de mejorar el proceso de enseñanza y aprendizaje.<br>
                    Evaluar la viabilidad de la metodología educativa gamificada en dos instituciones adscritas al programa de técnico en programación de software perteneciente al Centro de Servicios y Gestión Empresarial.<br> </p>
                    <h3 class=" Mitexto mt-5">Marco Teórico
                    </h3><br>
                    <p class="text-muted align-text-justify">Se hace referencia a los conceptos propios de la investigación tales como la educación emocional, ya que es una innovación educativa que responde a necesidades sociales y son objeto de estudio, sus principios, como elemento activo en la formación integral de la personalidad, el contexto de la aplicación de la misma, donde se hace referencia los momentos, espacios y entornos donde se fundamenta la educación emocional como estrategia de mejoramiento en el proceso educativo. <br>

                    En este capítulo se hace referencia a los factores de estudio que se desea abordar y a los cuales se pretende dar solución, estos son la frustración y la deserción.</p>
                    <h3 class=" Mitexto mt-5">Diseño Metodológico </h3><br>
                    <p class="text-muted align-text-justify">El estudio se efectúa en el marco de la investigación mixta, cuyo objetivo principal implica la mezcla de dos enfoques, el cualitativo y el cuantitativo, según Guelmes Valdés & Nieto Almeida (2015) “el enfoque mixto es un proceso que recolecta, analiza y vincula datos cuantitativos y cualitativos en una misma investigación para responder a un planteamiento y obtener una comprensión e interpretación, lo más amplia posible, del fenómeno en estudio”, además ha sido utilizado como un método de comprensión, descripción y explicación de estudios e investigaciones educativos, centrado en una búsqueda constante por aumentar conocimientos y transformar positivamente la realidad que se percibe (Núñez Moscoso, 2017 & Pereira Pérez, 2011). </p>

